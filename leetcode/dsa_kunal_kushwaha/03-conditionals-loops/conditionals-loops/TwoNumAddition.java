import java.util.Scanner;

public class TwoNumAddition {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter two numbers : ");
        int n1 =sc.nextInt();
        int n2 = sc.nextInt();
        System.out.println("Sum is : "+(n1+n2));
    }
}
